#include "push_swap.h"

t_list	*ft_lstnew(int content)
{
	t_list	*new;

	new = (t_list *)malloc(sizeof(t_list));
	if (!new)
		return (NULL);
	new->next = NULL;
	new->prev = NULL;
	new->target = NULL;
	new->ismax = 0;
	new->ismax = 0;
	new->content = content;
	return (new);
}
